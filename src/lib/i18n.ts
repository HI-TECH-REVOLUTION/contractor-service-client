export const useI18n = create<I18nState>()(
	persist(
		(set, get) => ({
			locale: "vi" as Locale,
			setLocale: (locale: Locale) => set({ locale }),
			t: (key: string) => {
				const { locale } = get();
				return translations[locale][key] || key;
			},
		}),
		{ name: "vertex-i18n" },
	),
);
